// Helper functions for API-like operations
export const getPropertyById = (id: string): Property | undefined => {
  return properties.find(p => p.id === id);
};

export const getPropertiesByCounty = (judetSlug: string): Property[] => {
  return properties.filter(p => p.judetSlug === judetSlug);
};

export const getPropertiesByOwner = (ownerId: string): Property[] => {
  return properties.filter(p => p.ownerId === ownerId);
};

export const getPropertiesByType = (type: PropertyType): Property[] => {
  return properties.filter(p => p.type === type);
};

export const searchProperties = (query: string): Property[] => {
  const lowerQuery = query.toLowerCase();
  return properties.filter(p => 
    p.name.toLowerCase().includes(lowerQuery) ||
    p.city.toLowerCase().includes(lowerQuery) ||
    p.description.toLowerCase().includes(lowerQuery)
  );
};
